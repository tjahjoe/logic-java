package latihanUTSsem22;

public class Mahasiswa {
    String nisn;
    String nama;
    String alamat;
    int tahun;
    double nilai;

    Mahasiswa(String nisn, String nama, String alamat, int tahun, double nilai){
        this.nisn = nisn;
        this.nama = nama;
        this.alamat = alamat;
        this.tahun = tahun;
        this.nilai = nilai;
    }
}
